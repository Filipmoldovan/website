// Función para obtener una ciudad por su slug
export function obtenerCiudadPorSlug(slug: string): Ciudad | undefined {
  return ciudades.find((ciudad) => ciudad.slug === slug)
}

// Función para obtener todas las zonas de una ciudad
export function obtenerZonasDeCiudad(ciudadSlug: string): string[] {
  const ciudad = obtenerCiudadPorSlug(ciudadSlug)
  return ciudad ? ciudad.zonas : []
}
